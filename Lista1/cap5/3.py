n1 = float(input("Informe um numero: "))
n2 = float(input("Informe outro numero: "))

soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2

print(f"{n1} + {n2} = {soma}")
print(f"{n1} - {n2} = {subtracao}")
print(f"{n1} * {n2} = {multiplicacao}")
print(f"{n1} / {n2} = {divisao}")
